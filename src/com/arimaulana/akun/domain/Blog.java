package com.arimaulana.akun.domain;

/**
 *
 * @author AriMaulana99
 */
public class Blog {

    private Long id;
    private String email;
    private String nama;
    private String url;

    public Blog() {
    }

    public Blog(Long id, String email, String nama, String url) {
        this.id = id;
        this.email = email;
        this.nama = nama;
        this.url = url;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    @Override
    public String toString() {
        return "Blog{" + "id=" + id + ", email=" + email + ", nama=" + nama + ", url=" + url + '}';
    }

}

package com.arimaulana.akun.domain;

/**
 *
 * @author AriMaulana99
 */
public class Bbm {

    private Long id;
    private String email;
    private String nama;
    private String pin;
    private String password;

    public Bbm() {
    }

    public Bbm(Long id, String email, String nama, String pin, String password) {
        this.id = id;
        this.email = email;
        this.nama = nama;
        this.pin = pin;
        this.password = password;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getPin() {
        return pin;
    }

    public void setPin(String pin) {
        this.pin = pin;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    @Override
    public String toString() {
        return "Bbm{" + "id=" + id + ", email=" + email + ", nama=" + nama + ", pin=" + pin + ", password=" + password + '}';
    }

}
